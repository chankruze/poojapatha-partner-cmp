package geekofia.poojapatha.partner.cmp.ui.theme

/**
 * Canonical spacing scale used throughout the app.
 *
 * Usage:  Modifier.padding(Spacing.lg)
 *         Arrangement.spacedBy(Spacing.md)
 */
object Spacing {
    /** 4 dp — tight separation between related elements */
    val xs = 4
    /** 8 dp — default gap between list items / icon + label */
    val sm = 8
    /** 12 dp — inner card padding (compact) */
    val md = 12
    /** 16 dp — standard screen horizontal margin */
    val lg = 16
    /** 20 dp — section gap */
    val xl = 20
    /** 24 dp — between major sections */
    val xxl = 24
    /** 32 dp */
    val xxxl = 32
    /** 48 dp — hero/banner padding */
    val huge = 48
    /** 64 dp — large empty-state illustrations */
    val giant = 64
}
